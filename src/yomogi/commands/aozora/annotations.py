SOURCE_LOCATION_PATTERN = re.compile(r"、\d+-\d+$")
INDENT_PATTERN = re.compile(r"(?P<count>[０-９]+)字下げ")

REMOVE_PATTERNS = (
    r".*?は(?:中|大)見出し",
    r"改(?:ページ|頁)",
    r"傍点(?:終わり)?",
    r".*?に傍点",
    r".*?は下付き小文字",
    r"ここから[０-９]+字下げ",
    r"ここで字下げ終わり",
    r"地から[０-９]+字上げ",
)
REMOVE_PATTERN = re.compile(r"(?:" + "|".join(REMOVE_PATTERNS) + r")")

FULLWIDTH_DIGITS = str.maketrans("０１２３４５６７８９", "0123456789")


def indentation(match: re.Match[str]) -> str:
    count = int(match.group("count").translate(FULLWIDTH_DIGITS))
    return "　" * count


def replace_annotation(content: str) -> str | None:
    normalized_content = SOURCE_LOCATION_PATTERN.sub("", content)
    if normalized_content in EXACT_REPLACEMENTS:
        return EXACT_REPLACEMENTS[normalized_content]

    indent_match = INDENT_PATTERN.fullmatch(content)
    if indent_match:
        return indentation(indent_match)

    if REMOVE_PATTERN.fullmatch(content):
        return ""
    return None
